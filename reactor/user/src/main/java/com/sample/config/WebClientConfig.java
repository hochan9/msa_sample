@Configuration
public class WebClientConfig {
  @Value("${api.url.wallet}")
  private String walletUrl;

  @Bean
  public WebClient walletWebClient() {
    return WebClient.builder()
            .baseUrl(walletUrl)
            .build();

  }
}
